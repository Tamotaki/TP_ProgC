#include <stdio.h>

typedef struct {
    unsigned char R;
    unsigned char G;
    unsigned char B;
    unsigned char A;
} Couleur;

int main() {
    Couleur couleurs[10] = {
        {0xef,0x78,0x12,0xff}, {0x2c,0xc8,0x64,0xff}, {0x12,0x34,0x56,0xff},
        {0x78,0x9a,0xbc,0xff}, {0xde,0xad,0xbe,0xff}, {0xfa,0xce,0xb0,0xff},
        {0x0f,0x1e,0x2d,0xff}, {0x33,0x44,0x55,0xff}, {0xaa,0xbb,0xcc,0xff},
        {0x11,0x22,0x33,0xff}
    };

    for(int i=0; i<10; i++) {
        printf("Couleur %d:\nRouge: %d\nVert: %d\nBleu: %d\nAlpha: %d\n\n",
               i+1, couleurs[i].R, couleurs[i].G, couleurs[i].B, couleurs[i].A);
    }

    return 0;
}
